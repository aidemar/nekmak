function AC_AddExtension(src, ext)
{
  if (src.indexOf('?') != -1)
    return src.replace(/\?/, ext+'?'); 
  else
    return src + ext;
}

function AC_Generateobj(objAttrs, params, embedAttrs) 
{ 
  var str = '<object ';
  for (var i in objAttrs)
    str += i + '="' + objAttrs[i] + '" ';
  str += '>';
  for (var i in params)
    str += '<param name="' + i + '" value="' + params[i] + '" /> ';
  str += '<embed ';
  for (var i in embedAttrs)
    str += i + '="' + embedAttrs[i] + '" ';
  str += ' ></embed></object>';

  //document.write(str);

	if(isIE || isNS6) { 
		document.getElementById('ieFlashLayer').innerHTML = str;
	} else if(isNS4) {
		document.layers['nsFlashLayer'].document.open();
		document.layers['nsFlashLayer'].document.write(str);
		document.layers['nsFlashLayer'].document.close(); 
	}
}

